public class RelationalOperator {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 7;
        int num3 = 10;

        System.out.println("num1 > num2 : " +(num1>num2));
        System.out.println("num1 < num2 : " +(num1<num2));
        System.out.println("num1 >= num2 : " +(num1>=num2));
        System.out.println("num1 <= num2 : " +(num1<=num2));
        System.out.println("num1 == num2 : " +(num1==num3));
        System.out.println("num1 != num2 : " +(num1!=num3));
    }
}
